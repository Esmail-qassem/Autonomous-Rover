/*Global Interrupt Enable*/
 void GIE_VoidEnable(void)
 {  
    SET_BIT(SREG,SREG_I);
 } 
 
/*Global Interrupt Disable*/

 void GIE_VoidDisable(void)
 {
	 CLEAR_BIT(SREG,SREG_I);
 }
